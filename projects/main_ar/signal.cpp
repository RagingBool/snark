void Lfo::update(uint16_t deltaMillis) {
  _phaseGenerator.update(deltaMillis);
  int phase = _phaseGenerator.getPhase();
  uint8_t triangle = (phase < 0x100) ? phase : 0x1FF - phase;
  
  switch(_lfoFunction) {
    case SAW_UP:
      _value = phase >> 1;
      break;
    case SAW_DOWN:
      _value = 0xFF - (phase >> 1);
      break;
    case SQUARE:
      _value = (phase < 0x100) ? 0 : 0xFF;
      break;
    case TRIANGLE:
      _value = triangle;
      break;
    case SIN_WAVE:
      _value = pgm_read_byte(&sinWave[triangle]);
      break;
    default:
      _value = 0;
      break;
  }
}
